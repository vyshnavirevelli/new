class Unaryoperators{
public static void main(String args[]){
int a=10;
System.out.println("unary minus "+ (-a));
System.out.println("unary pre increment "+ (++a));
System.out.println("unary post increment "+ (a++));
System.out.println("unary after post increment "+ (a));
System.out.println("unary predecrement "+ (--a));
System.out.println("unary postdecrement "+ (a--));
System.out.println("unary after postdecrement "+(a));
}
}